About Us

Welcome to the On-Line-Interactivity Web Based IDE Project

We invite you to apply for a new account if you have an interest in learning how to create web applications and web sites.

This site is powered by the CODIAD Web IDE to allow you to create projects and edit the code with out needing any client desktop programming tools.
This WebIDE allows you and your team to work on the same document remotely at the same time collaboratively. 
This provides you with a fantastic tool to build working code and see it run instantly.

We have meetings the first Saturday of every month from 9 am to noon to share our projects and get help from other members. Members that want to use our on-line IDE tool will need to attend a meeting to have the access activated.

We meet at Tullys in the Canyon Park Plaza in Bothell: 22615 Bothell Everett Hwy Bothell WA 98021

